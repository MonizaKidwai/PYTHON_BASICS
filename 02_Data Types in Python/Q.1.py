# Create variables of different data types: integer, float, string, boolean, list, tuple, set, and dictionary. Print their types using type().
# Different data types
a = 10               # Integer
b = 3.14             # Float
c = "Hello"          # String
d = True             # Boolean
e = [1, 2, 3]        # List
f = (4, 5, 6)        # Tuple
g = {7, 8, 9}        # Set
h = {"name": "Aisha", "age": 20}  # Dictionary

# Printing types
print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))
print(type(f))
print(type(g))
print(type(h))